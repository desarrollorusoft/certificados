            break;

            case 'revelador_tipo_liquido':
               
              return TipoLiquidos::where('revelador_sn',1)->get();

            break;

            case 'removedor_tipo_liquido':
               
              return TipoLiquidos::where('removedor_sn',1)->get();

            break;            
          
        }
    }

